func main() {
	dbURL := os.Getenv("DATABASE_URL")
	if dbURL == "" {
		log.Fatal("DATABASE_URL not set")
	}

	ctx := context.Background()
	pool, err := pgxpool.New(ctx, dbURL)
	if err != nil {
		log.Fatalf("connect: %v", err)
	}
	defer pool.Close()

	for _, co := range courses {
		if err := insertCourse(ctx, pool, co); err != nil {
			log.Fatalf("seed course %q: %v", co.slug, err)
		}
		fmt.Printf("OK: course %q seeded (%d items)\n", co.slug, len(co.verses))
	}

	totalItems, err := insertCourseWithSections(ctx, pool, warmup)
	if err != nil {
		log.Fatalf("seed course %q: %v", warmup.slug, err)
	}
	fmt.Printf("OK: course %q seeded (%d sections, %d items)\n", warmup.slug, len(warmup.sections), totalItems)

	skipped, totalProphecy, err := insertProphecyCourse(ctx, pool)
	if err != nil {
		log.Fatalf("seed messiah-prophecy: %v", err)
	}
	fmt.Printf("OK: course \"messiah-prophecy\" seeded (%d sections, %d items)\n", len(messiahProphecySections), totalProphecy)
	if len(skipped) > 0 {
		fmt.Printf("WARN: %d절 조회 실패 (데이터 확인 필요):\n", len(skipped))
		for _, s := range skipped {
			fmt.Println(" ", s)
		}
	}
}

func insertProphecyCourse(ctx context.Context, pool *pgxpool.Pool) (skipped []string, total int, err error) {
	var courseID int64
	err = pool.QueryRow(ctx, `
		INSERT INTO courses(slug, title, theme, ord)
		VALUES ('messiah-prophecy', '구약에 나타난 메시야 예언', 'messiah', 33)
		ON CONFLICT (slug) DO UPDATE SET slug = EXCLUDED.slug
		RETURNING id
	`).Scan(&courseID)
	if err != nil {
		return nil, 0, fmt.Errorf("upsert course: %w", err)
	}

	for secOrd, sec := range messiahProphecySections {
		var sectionID int64
		err = pool.QueryRow(ctx, `
			INSERT INTO course_sections(course_id, title, ord)
			VALUES ($1, $2, $3)
			ON CONFLICT (course_id, ord) DO UPDATE SET title = EXCLUDED.title
			RETURNING id
		`, courseID, sec.title, secOrd+1).Scan(&sectionID)
		if err != nil {
			return nil, 0, fmt.Errorf("upsert section %q: %w", sec.title, err)
		}

		itemOrd := 0
		for _, pr := range sec.props {
			allRefs := append(pr.ot, pr.nt...)
			for _, ref := range allRefs {
				var verseID int64
				lookupErr := pool.QueryRow(ctx,
					`SELECT id FROM bible_verses WHERE book=$1 AND chapter=$2 AND verse=$3`,
					ref.b, ref.c, ref.v,
				).Scan(&verseID)
				if lookupErr != nil {
					skipped = append(skipped, fmt.Sprintf("%s — %d:%d:%d (%s)", pr.desc, ref.b, ref.c, ref.v, lookupErr))
					continue
				}
				itemOrd++
				_, insertErr := pool.Exec(ctx, `
					INSERT INTO course_items(course_id, section_id, verse_id, ord, topic)
					VALUES ($1, $2, $3, $4, $5)
					ON CONFLICT (section_id, ord) WHERE section_id IS NOT NULL
					DO UPDATE SET verse_id = EXCLUDED.verse_id, topic = EXCLUDED.topic
				`, courseID, sectionID, verseID, itemOrd, pr.desc)
				if insertErr != nil {
					return nil, 0, fmt.Errorf("insert item (section %q, verse %d:%d:%d): %w", sec.title, ref.b, ref.c, ref.v, insertErr)
				}
				total++
			}
		}
	}
	return skipped, total, nil
}

func insertCourse(ctx context.Context, pool *pgxpool.Pool, co seedCourse) error {
	var courseID int64
	err := pool.QueryRow(ctx, `
		INSERT INTO courses(slug, title, theme, ord, hidden)
		VALUES ($1, $2, $3, $4, $5)
		ON CONFLICT (slug) DO UPDATE SET hidden = EXCLUDED.hidden
		RETURNING id
	`, co.slug, co.title, co.theme, co.ord, co.hidden).Scan(&courseID)
	if err != nil {
		return fmt.Errorf("upsert course: %w", err)
	}

	for i, vr := range co.verses {
		var verseID int64
		err := pool.QueryRow(ctx,
			`SELECT id FROM bible_verses WHERE book=$1 AND chapter=$2 AND verse=$3`,
			vr.book, vr.chapter, vr.verse,
		).Scan(&verseID)
		if err != nil {
			return fmt.Errorf("lookup verse %d:%d:%d: %w", vr.book, vr.chapter, vr.verse, err)
		}

		_, err = pool.Exec(ctx, `
			INSERT INTO course_items(course_id, verse_id, ord, topic)
			VALUES ($1, $2, $3, $4)
			ON CONFLICT (course_id, verse_id) WHERE section_id IS NULL DO NOTHING
		`, courseID, verseID, i+1, vr.topic)
		if err != nil {
			return fmt.Errorf("insert course_item: %w", err)
		}
	}

	return nil
}

